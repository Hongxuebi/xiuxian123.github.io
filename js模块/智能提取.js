/**
 * 检测句子包含的语气方向
 */
function 检测语气(句) {
  if (/不[喜欢爱吃喝看听玩去享受想会能可以应该该要]|讨厌|烦|丑|难看|受不了|无法接受|差|垃圾|不好|太差/.test(句)) return '否定';
  if (/喜欢|爱|很棒|不错|好[好看吃听用玩喝]|推荐|超好|很[好棒赞强爽]|太[好棒赞强爽喜欢了]|有[意思趣质量]/.test(句)) return '肯定';
  return '中性';
}

/**
 * 对单条句子进行多维评分
 */
function 评分句子(句) {
  const 语气 = 检测语气(句);
  const 分数 = {};
  let 最佳类型 = null;
  let 最高分 = 0;
  let 最佳规则 = null;

  for (const 规则 of 萃取规则) {
    // 语气过滤（规则只匹配某些语气时）
    if (规则.语气 && !规则.语气.includes(语气)) continue;

    // 条件过滤
    if (规则.条件 && !规则.条件(句)) continue;

    let 命中数 = 0;
    for (const 词 of 规则.词库) {
      // 模糊匹配 — 用词在句中存在就算命中
      if (句.includes(词)) 命中数++;
    }
    if (命中数 === 0) continue;

    const 总分 = 规则.基数分 + 命中数 * 规则.单次分;

    // 否定语气在偏好/事件类上加分（表达强烈偏好）
    let 最终分 = 总分;
    if (语气 === '否定' && (规则.类型 === 'preference' || 规则.类型 === 'event')) {
      最终分 += 2;
    }

    if (!分数[规则.类型]) 分数[规则.类型] = 0;
    分数[规则.类型] += 最终分;

    if (最终分 > 最高分) {
      最高分 = 最终分;
      最佳类型 = 规则.类型;
      最佳规则 = 规则;
    }
  }

  // 兜底：如果没有任何规则命中但有 "我" 字且 >=6个字，可能是隐含个人信息的陈述
  if (!最佳类型 && 句.includes('我') && 句.length > 6) {
    // 做技术推断：含技术词且不是简单问答
    const 技术词数 = 话题词库.reduce((sum, t) => sum + t.词库.filter(w => 句.includes(w)).length, 0);
    if (技术词数 >= 2) {
      最佳类型 = 'fact';
      最高分 = 4;
    }
  }

  return { 最佳类型, 分数: 最高分, 语气 };
}

/**
 * 话题聚类推断 — 从对话历史中发现重复话题
 */
function 话题推断(所有用户句) {
  const 话题命中 = {};
  for (const 句 of 所有用户句) {
    for (const 话题 of 话题词库) {
      const 命中数 = 话题.词库.filter(w => 句.includes(w)).length;
      if (命中数 > 0) {
        if (!话题命中[话题.话题]) 话题命中[话题.话题] = { 命中: 0, 来源句: [] };
        话题命中[话题.话题].命中 += 命中数;
        话题命中[话题.话题].来源句.push(句.slice(0, 30));
      }
    }
  }
  // 返回累计命中 >=3 的热门话题
  return Object.entries(话题命中)
    .filter(([, v]) => v.命中 >= 3)
    .sort(([, a], [, b]) => b.命中 - a.命中)
    .slice(0, 3);
}

/**
 * 智能提取记忆 - 核心入口（从最近对话中萃取四类信息）
 */
window.智能提取记忆 = async function() {
  const 管理器 = window.AI记忆管理器;
  if (!管理器) throw new Error('记忆系统未初始化');
  
  const 消息列表 = window.当前对话历史;
  if (!消息列表 || 消息列表.length < 2) {
    console.log('[萃取] 对话历史太短，跳过');
    return { facts: [], preferences: [], relationships: [], events: [], 类型数: 0, 总数: 0 };
  }
  
  // 只分析最近 20 条用户消息
  const 用户消息 = 消息列表.slice(-40).filter(m => m.role === 'user');
  if (用户消息.length < 1) {
    console.log('[萃取] 没有用户消息，跳过');
    return { facts: [], preferences: [], relationships: [], events: [], 类型数: 0, 总数: 0 };
  }

  const 结果 = { facts: [], preferences: [], relationships: [], events: [] };
  const 所有句子 = [];

  // ============ 阶段1：逐句评分萃取 ============
  for (const 消息 of 用户消息) {
    const 内容 = 消息.content || '';
    if (typeof 内容 !== 'string' || 内容.length < 3) continue;

    // 拆成句子（中文句号/问号/感叹号/换行/问号变体）
    const 句子们 = 内容.split(/[。！？\n?？]/).filter(s => s.trim().length > 5);
    
    for (const 原始句子 of 句子们) {
      const 句 = 原始句子.trim();
      if (句.length > 120) continue;

      // 元对话硬过滤：自我指涉/系统指令不入库
      if (/当用户说|当用户问|当我看到|我应该|我需要判断|你需要|你应该|记住，当|理解我的意思/.test(句)) continue;
      // 工具操作指令不入库
      if (/帮我切换|帮我打开|帮我关闭|帮我设置/.test(句) && 句.length < 30) continue;

      所有句子.push(句);

      // 去重检查
      const 已存在 = await 管理器.搜索(句.slice(0, 15), 3);
      if (已存在.some(m => m.内容 && m.内容.includes(句.slice(0, 8)))) continue;

      // 多维评分
      const { 最佳类型, 分数: 重要性 } = 评分句子(句);
      if (!最佳类型) continue;

      await 管理器.记住(句, 最佳类型, 重要性);
      结果[最佳类型].push(句);
    }
  }

  // ============ 阶段2：话题聚类推断（热门话题→隐含偏好） ============
  const 热门话题 = 话题推断(所有句子);
  for (const [话题名, 数据] of 热门话题) {
    // 构造一条隐含偏好
    const 推断句 = `（推断）用户关注${话题名}相关话题`;
    
    // 检查是否已有此类推断
    const 已存在推断 = await 管理器.搜索(话题名, 3);
    if (已存在推断.some(m => m.内容 && m.内容.includes('推断') && m.内容.includes(话题名))) continue;

    await 管理器.记住(推断句, 'preference', 5);
    结果.preferences.push(推断句);
  }

  const 总数 = 结果.facts.length + 结果.preferences.length + 结果.relationships.length + 结果.events.length;

  console.log(
    '[萃取] 完成：事实%d 偏好%d 关系%d 事件%d 总计%d',
    结果.facts.length, 结果.preferences.length,
    结果.relationships.length, 结果.events.length, 总数
  );

  if (热门话题.length > 0) {
    console.log('[萃取] 话题推断:', 热门话题.map(([t]) => t).join(', '));
  }

  // 记录萃取时间
  localStorage.setItem('_last_extract_time', String(Date.now()));
  // 触发刷新事件（记忆列表 + 状态概览）
  window.dispatchEvent(new CustomEvent('记忆萃取完成'));

  结果.类型数 = Object.keys(结果).filter(k => 结果[k].length > 0).length;
  结果.总数 = 总数;
  return 结果;
};
